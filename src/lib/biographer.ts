import { supabaseAdmin } from "./supabaseAdmin";
import Anthropic from "@anthropic-ai/sdk";
import { logClaude } from "./usage";
import { getFinanceSummary } from "./finance";
import { getBiographerHistory } from "./queries";
import { recallContext } from "./semanticMemory";
import { getChatVoice, voiceLine } from "./chatVoice";

// Отвечает на вопрос пользователя по его записям (ассистент «спроси свою жизнь»).
const ASK_LANG: Record<string, string> = { ru: "русском", en: "английском (English)", uk: "українській", fr: "французском (français)" };

export async function askLife(userId: string, question: string, lang: string = "ru"): Promise<string> {
  const db = supabaseAdmin();
  const [{ data: entries }, finance] = await Promise.all([
    db
      .from("entries")
      .select("entry_date, summary, raw_text")
      .eq("user_id", userId)
      .order("entry_date", { ascending: true })
      .limit(200),
    getFinanceSummary(userId).catch(() => ""),
  ]);

  const list = (entries || []).map((e) => `${e.entry_date}: ${(e.raw_text || e.summary || "").slice(0, 800)}`).join("\n") || "(записей пока нет)";

  // Поиск по смыслу: подтягиваем самые релевантные записи под вопрос, в т.ч.
  // старее последних 200 (мягко "" без pgvector/эмбеддингов).
  const recall = await recallContext(userId, question).catch(() => "");
  const voice = await getChatVoice(userId); // тон и стиль общения из профиля

  // Также подмешиваем личную Базу знаний (сохранённое из Instagram/YouTube: рецепты,
  // тренировки, советы), чтобы ассистент отвечал и по ней, а не только по дневнику.
  let saved = "(пусто)";
  try {
    const { data: items } = await db
      .from("saved_items")
      .select("title, topic, summary, key_points")
      .eq("user_id", userId)
      .order("created_at", { ascending: false })
      .limit(200);
    if (items?.length) {
      saved = items
        .map((it: any) => {
          const pts = Array.isArray(it.key_points) && it.key_points.length ? "; " + it.key_points.join("; ") : "";
          return `• ${it.title || "—"}${it.topic ? ` [${it.topic}]` : ""}: ${(it.summary || "").slice(0, 500)}${pts}`.slice(0, 900);
        })
        .join("\n");
    }
  } catch {
    // таблицы может не быть — не страшно
  }

  // Визуальная память: распознанные фото и документы (чеки, договоры, свидетельства, техпаспорта…),
  // чтобы ассистент находил по запросу сами файлы и данные из них.
  let docs = "(пусто)";
  try {
    const { data: mems } = await db
      .from("memories")
      .select("category, title, summary, fields, mem_date, created_at")
      .eq("user_id", userId)
      .order("created_at", { ascending: false })
      .limit(200);
    if (mems?.length) {
      docs = mems
        .map((m: any) => {
          const fs = Array.isArray(m.fields) && m.fields.length ? " — " + m.fields.map((f: any) => `${f.label}: ${f.value}`).join(", ") : "";
          return `• [${m.category || "other"}] ${m.title || "—"}${m.mem_date ? ` (${m.mem_date})` : ""}: ${(m.summary || "").slice(0, 400)}${fs}`.slice(0, 900);
        })
        .join("\n");
    }
  } catch {
    // таблицы может не быть — не страшно
  }

  // Недавний диалог: даём ассистенту память о последних сообщениях — в т.ч. о
  // собственных утренних пушах, — чтобы он связывал уточняющие вопросы с контекстом.
  let convo = "(пусто)";
  try {
    const hist = await getBiographerHistory(userId, 6);
    if (hist.length) {
      convo = hist.slice().reverse()
        .map((h: any) => {
          const a = (h.answer || "").slice(0, 500);
          // Утренние пуши помечены спецметкой — показываем их как твоё сообщение, без «вопроса».
          if ((h.question || "").startsWith("☀️")) return `Ты (утреннее сообщение): ${a}`;
          return `Пользователь: ${(h.question || "").slice(0, 300)}\nТы: ${a}`;
        })
        .join("\n---\n");
    }
  } catch {
    // история необязательна
  }

  const prompt = `Ты — личный AI-ассистент в LIFE OS. Пользователь задаёт вопрос. Ответь ПО СУЩЕСТВУ и по делу, ВСЕГДА на ${ASK_LANG[lang] || ASK_LANG.ru} языке (даже если вопрос задан на другом языке), опираясь на источники ниже: ДНЕВНИК (что с пользователем происходило), БАЗА ЗНАНИЙ (полезные материалы, сохранённые из Instagram/YouTube — рецепты, тренировки, советы), ВИЗУАЛЬНАЯ ПАМЯТЬ (распознанные фото и загруженные документы — чеки, договоры, свидетельства, техпаспорта, справки, со всеми извлечёнными данными), ФИНАНСЫ (сводка доходов и расходов по годам и статьям) и НЕДАВНИЙ ДИАЛОГ (последние сообщения между тобой и пользователем).

ВАЖНО:
- НИКОГДА не отвечай, что «не можешь открыть ссылку» или «нет доступа к интернету». Ты НЕ ходишь по ссылкам — весь нужный контент уже извлечён и лежит ниже в БАЗЕ ЗНАНИЙ. Отвечай по нему.
- Если пользователь говорит «это видео / этот пост / этот ролик / отсюда» без уточнения — он почти наверняка про САМЫЙ ПЕРВЫЙ (самый свежий) материал в БАЗЕ ЗНАНИЙ ниже. Бери его.
- Материалы могут быть на другом языке — ты их понимаешь и переводишь смысл.
- «как сделать / дай рецепт / всю информацию из видео» — ищи в БАЗЕ ЗНАНИЙ.
- Вопросы про документы, файлы, чеки, договоры, свидетельства, номера, VIN, даты, суммы из загруженного («найди техпаспорт», «какой VIN», «когда договор», «найди свидетельство») — ищи в ВИЗУАЛЬНОЙ ПАМЯТИ и приводи конкретные данные оттуда.
- Для вопросов про деньги, доходы, расходы, заработок, траты по годам и категориям — используй блок ФИНАНСЫ и приводи суммы с валютой.
- Если просят ПЕРЕЧИСЛИТЬ траты/доходы за месяц («покажи расходы за июль», «какие расходы в этом месяце») — бери операции ТОЛЬКО из раздела «ОПЕРАЦИИ ПОИМЁННО» блока ФИНАНСЫ (реальный кошелёк «Деньги»), а НЕ из текста дневника. Перечисляй КАЖДУЮ операцию запрошенного месяца одной строкой «• категория (или заметка) — сумма валюта» и НЕ подмешивай соседние месяцы. Не придумывай подписи столбцов и не склеивай слова (никаких «СуммаКафе»); одна строка = одна реальная операция.
- КРИТИЧНО про сходимость: сумма перечисленных тобой операций ОБЯЗАНА равняться расходу этого месяца из строки «ПО МЕСЯЦАМ». Строка «ПО МЕСЯЦАМ» показывает лишь «крупнейшие статьи» (топ-несколько) — это НЕ полный список, НЕ выдавай эти 3-4 статьи за все расходы: тогда сумма не сойдётся с итогом. Если поимённых операций на весь итог месяца нет (в «ОПЕРАЦИИ ПОИМЁННО» их меньше, чем итог), честно скажи: «итог за месяц — X, но детальных операций видно только на Y; остальное — из общей сводки без разбивки», и НЕ подставляй недостающую сумму выдуманными позициями.
- Если уместно — ссылайся на даты записей дневника.
- Если вопрос ссылается на недавнее («а что за…», «о чём ты», «поясни», «какие именно») — сначала загляни в НЕДАВНИЙ ДИАЛОГ: скорее всего речь о ТВОЁМ же прошлом сообщении (в т.ч. утреннем пуше). Установи связь и ответь по нему, а не «у меня такого нет». Но и тут не выдумывай: поясняй ровно тем, что реально стоит за твоими словами в источниках; если сам был неточен — честно уточни у пользователя.
- Если в источниках реально нет ответа — честно скажи об этом (но не ссылайся на «отсутствие доступа»). Не выдумывай.
СТИЛЬ ОТВЕТА (пользователь выбрал его сам в профиле): ${voiceLine(voice.tone, voice.style)}. Держись этого стиля. Пиши живо, по-человечески, без воды — но факты и достоверность всегда важнее стиля.

${recall ? recall + "\n\n" : ""}ДНЕВНИК (дата: текст):
${list}

БАЗА ЗНАНИЙ (сохранённое из Instagram/YouTube):
${saved}

ВИЗУАЛЬНАЯ ПАМЯТЬ (распознанные фото и документы):
${docs}

${finance || "ФИНАНСЫ: операций пока нет."}

НЕДАВНИЙ ДИАЛОГ (последние сообщения, новые внизу; «Ты» — это твои же прошлые сообщения, включая утренние пуши):
${convo}

ВОПРОС: ${question}`;

  const m = await new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY }).messages.create({
    model: "claude-sonnet-4-6",
    max_tokens: 1000,
    messages: [{ role: "user", content: prompt }],
  });
  logClaude(userId, "biographer", "sonnet", (m as any).usage);
  return m.content.filter((b) => b.type === "text").map((b: any) => b.text).join("\n").trim();
}

export async function saveChat(userId: string, question: string, answer: string) {
  try {
    await supabaseAdmin().from("biographer_chats").insert({ user_id: userId, question, answer });
  } catch {
    // история необязательна
  }
}
