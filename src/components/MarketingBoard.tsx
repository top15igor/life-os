function CopyBtn({ text }: { text: string }) {
  const [done, setDone] = useState(false);
  return (
    <button
      onClick={async () => { try { await navigator.clipboard.writeText(text); setDone(true); setTimeout(() => setDone(false), 1600); } catch {} }}
      style={{ display: "inline-flex", alignItems: "center", gap: 6, padding: "6px 12px", borderRadius: 9, border: "1px solid var(--border)", background: done ? "var(--accent-bg)" : "var(--surface)", color: done ? "var(--accent-text)" : "var(--accent)", fontSize: 12.5, cursor: "pointer", fontWeight: 500 }}>
      <i className={`ti ${done ? "ti-check" : "ti-copy"}`} style={{ fontSize: 14 }} />{done ? "Скопировано" : "Копировать"}
    </button>
  );
}

